/// <summary>
/// Результат срабатывания хука.
/// </summary>
public enum HookResult
{
    /// <summary>
    /// Событие не обрабатывалось, продолжить как обычно.
    /// </summary>
    Continue,

    /// <summary>
    /// Событие отменено, стандартная обработка не выполняется.
    /// </summary>
    Cancel,

    /// <summary>
    /// Событие обработано, дальнейшая обработка не требуется.
    /// </summary>
    Handled,

    /// <summary>
    /// Событие модифицировано (например, изменён урон, эффект, количество).
    /// </summary>
    Modified,

    /// <summary>
    /// Событие полностью обработано и дальнейшая обработка запрещена,
    /// но стандартная логика может выполняться в ограниченном виде.
    /// </summary>
    HandledMain
}


