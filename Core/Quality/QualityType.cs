/// <summary>
/// Перечисление, описывающее редкость или качество предметов, персонажей или других объектов.
/// Используется для определения вероятности выпадения, ценности или силы.
/// </summary>
public enum QualityType
{
    /// <summary>
    /// Обычное качество, самый распространённый уровень.  
    /// Минимальная редкость и базовые характеристики.
    /// </summary>
    Common,

    /// <summary>
    /// Необычное качество, немного лучше обычного.  
    /// Может иметь дополнительные свойства или бонусы.
    /// </summary>
    Uncommon,

    /// <summary>
    /// Редкое качество, сложно найти.  
    /// Обычно сильнее или полезнее предыдущих уровней.
    /// </summary>
    Rare,

    /// <summary>
    /// Мифическое качество. Очень редкое, с уникальными характеристиками.
    /// </summary>
    Mythic,

    /// <summary>
    /// Эпическое качество. Сильные бонусы, заметно превосходит более низкие уровни.
    /// </summary>
    Epic,

    /// <summary>
    /// Легендарное качество. Эксклюзивные свойства, крайне редкое.
    /// </summary>
    Legendary,

    /// <summary>
    /// Древнее качество. Экстремально редкое, с уникальными эффектами.
    /// </summary>
    Ancient,

    /// <summary>
    /// Божественное качество. Наивысший уровень, почти невозможно получить.
    /// </summary>
    Godlike
}
