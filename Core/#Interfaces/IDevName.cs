/// <summary>
/// Определяет объект, который имеет внутреннее (техническое) имя, 
/// используемое разработчиками для идентификации в коде или отладке.
/// Имя должно быть уникальным среди всех объектов этого типа.
/// </summary>
/// <remarks>
/// Обычно <see cref="DevName"/> используется для системных целей, 
/// например, для поиска или логирования, и не предназначено для отображения игроку.
/// Пример: "Weapon_AK47", "Ammo_9mm", "Effect_Explosion_Large".
/// </remarks>
public interface IDevName
{
    /// <summary>
    /// Возвращает уникальное имя, используемое разработчиками 
    /// для идентификации объекта внутри проекта.
    /// </summary>
    string DevName { get; }
}
