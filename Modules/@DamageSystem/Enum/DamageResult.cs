/// <summary>
/// Результат обработки получения урона.
/// </summary>
public enum DamageResult
{
    /// <summary>
    /// Урон не был обработан (например, обработчик не смог определить результат).
    /// </summary>
    NotHandled,

    /// <summary>
    /// Атака не попала по цели (промах).
    /// </summary>
    Miss,

    /// <summary>
    /// Урон был полностью заблокирован (щит, броня или другое средство защиты).
    /// </summary>
    Blocked,

    /// <summary>
    /// Урон был успешно применён к цели.
    /// </summary>
    Damaged,

    /// <summary>
    /// Урон привёл к смерти цели.
    /// </summary>
    Killed
}
